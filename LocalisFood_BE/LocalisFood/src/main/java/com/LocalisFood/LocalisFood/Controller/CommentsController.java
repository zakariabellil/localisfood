package com.LocalisFood.LocalisFood.Controller;

import com.LocalisFood.LocalisFood.dto.CommentsDto;
import com.LocalisFood.LocalisFood.Service.CommentService;
import lombok.AllArgsConstructor;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.*;

import java.util.List;

import static org.springframework.http.HttpStatus.CREATED;
import static org.springframework.http.HttpStatus.OK;

@RestController
@RequestMapping("/api/comments/")
@AllArgsConstructor
public class CommentsController {
    private final CommentService commentService;

    @PostMapping
    public ResponseEntity<Void> createComment(@RequestBody CommentsDto commentsDto) {
        commentService.save(commentsDto);
        return new ResponseEntity<>(CREATED);
    }

    @GetMapping("/by-product/{productId}")
    public ResponseEntity<List<CommentsDto>> getAllCommentsForProduct(@PathVariable Long productId) {
        return ResponseEntity.status(OK)
                .body(commentService.getAllCommentsForProduct(productId));
    }

    @GetMapping("/by-user/{userName}")
    public ResponseEntity<List<CommentsDto>> getAllCommentsForUser(@PathVariable String userName){
        return ResponseEntity.status(OK)
                .body(commentService.getAllCommentsForUser(userName));
    }

}